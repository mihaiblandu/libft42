#include "../libft.h"

void rec(int n)
{
    if(n > 0)
    {
        rec(n/10);
        ft_putchar(n%10+'0');
    }
}
void ft_putnbr(int n)
{
   if(n < 0) {
       ft_putchar('-');
       if(n == -2147483648)
       {
            ft_putchar('2');
            rec(147483648);
       }
       else
           rec(-1*n);
   } else{
       rec(n);
   }
}