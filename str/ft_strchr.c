#include "../libft.h"
char*   ft_strchr(const char *s, int c)
{
    char    a;

    a = (char)c;
    while (1)
    {
        if (*s == a)
            return ((char *)s);
        if (!*s)
            break;
        s++;
    }
    return (NULL);
}