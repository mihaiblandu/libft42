#include "../libft.h"

char    *ft_strrchr(const char *s, int c)
{
    int i;

    i = ft_strlen(s);
    while(*s)
        s++;

    while(i > -1)
    {
        if(*s == c)
            return ((char *)s);
        s--;
        i--;
    }
    return (NULL);
}