#include "../libft.h"

char* ft_strrev(char *s)
{
    int     i;
    int     len;
    char    c;

    i = 0;
    len = ft_strlen(s) - 1;
    while(i <= (len / 2))
    {
        c = s[i];
        s[i] = s[len - i];
        s[len - i] = c;
        i++;
    }
    return (s);
}